export const BING_SEARCH_DESCRIPTION = `使用必应搜索引擎搜索中文互联网内容，并返回结构化搜索结果列表。

## 何时使用

- 需要搜索最新网络信息时
- 需要围绕某个主题查找候选网页时
- 需要先搜再配合 \`fetch_webpage\` 抓取详细内容时

## 何时不使用

- **不要用于抓取已知 URL 的正文**：这种情况请直接使用 \`fetch_webpage\`
- **不要用于搜索本地 Vault 内容**：本地内容请使用文件系统工具

## 可用字段

- **query**（必需）：搜索关键词或查询语句
- **count**（可选，默认 10）：返回结果数量，范围 1 到 50
- **offset**（可选，默认 0）：结果偏移量，用于翻页

## 返回值

返回 JSON 格式的搜索结果列表。每个结果通常包含 \`id\`、\`title\`、\`url\`、\`snippet\` 和可选的 \`displayUrl\`。

## 失败恢复

- 如果结果不理想，调整 \`query\` 后重试
- 如果只是想读取某个已知网页，不要继续重试 \`bing_search\`，应改用 \`fetch_webpage\`
- 如果是网络请求失败，可稍后重试

## 示例

\`\`\`json
{
  "query": "Obsidian MCP 工具定义",
  "count": 5,
  "offset": 0
}
\`\`\``;
